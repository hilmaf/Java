package lang;

public class LangMain {
    public static void main(String[] args) {
        /**
         * java.lang 패키지
         * Object : 모든 자바 객체의 부모 클래스
         * String : 문자열
         * Integer, Long, Double : 래퍼 타입, 기본형 데이터 타입을 객체화한 것
         * Class : 클래스 메타 정보
         * System : 시스템과 관련된 기본 기능들을 제공
         * import 구문 생략 가능
         */
        System.out.println("hello java"); // import 구문을 사용하지 않아도 System 클래스를 사용한 것
    }
}